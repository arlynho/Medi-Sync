
#Classe que gerencia os tipos de dados
class Medico():
    def __init__(self, identificator, nome, email, telefone, crm, nascimento, cpf):
        self.id = identificator
        self.nome = nome
        self.email = email
        self.telefone = telefone
        self.crm = crm
        self.dataNasc = nascimento
        self.cpf = cpf




